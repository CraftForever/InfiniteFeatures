package com.github.craftforever.infinitefeatures.util;

import java.awt.Color;

public class Plant
{
	public String name;
	public Color woodcolor;
	public Color plantcolor;
	public Color plankcolor;

	public Plant(String iname, Color iwoodcolor, Color iplantcolor, Color iplankcolor)
	{
		name = iname;
		woodcolor = iwoodcolor;
		plantcolor = iplantcolor;
		plankcolor = iplankcolor;
	}
		
}
